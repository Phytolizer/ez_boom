#[derive(Copy, Clone)]
pub(crate) struct State {
    pub(crate) sprite: Spritenum,
    pub(crate) frame: i64,
    pub(crate) tics: i64,
    pub(crate) action: crate::think::ActionF,
    pub(crate) nextstate: usize,
}

impl Default for State {
    fn default() -> Self {
        Self {
            sprite: Spritenum::TNT1,
            frame: 0,
            tics: 0,
            action: || (),
            nextstate: Statenum::NULL as usize,
        }
    }
}

